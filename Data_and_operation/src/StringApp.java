
public class StringApp {

	public static void main(String[] args) {
		System.out.println("Hello World"); //String 문자열 캐릭터의 집
		// System.out.println('Hello World'); // '' -> character 한 글자를 표현하는 문자 
		
		System.out.println("Hello "
				+ "World"); //줄 바꿈 아님
		System.out.println("Hello \nWorld "); //new line의 약자 줄바꿈 가능
		
		//escape
		System.out.println("Hello \"World\""); //땀표안에 땀표 넣기 
		
	}

}
